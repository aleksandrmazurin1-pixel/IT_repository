        getOwner: function() {
            return owner;
        },
        
        getBalance: function() {
            return balance;
        },
        
        deposit: function(amount) {
            balance += amount;
            let message = `Внесено ${amount}`;
            addToHistory(message);
            return `${message}. Баланс: ${balance}`;
        },
        
        withdraw: function(amount) {
            if (balance >= amount) {
                balance -= amount;
                let message = `Снято ${amount}`;
                addToHistory(message);
                return `${message}. Баланс: ${balance}`;
            } else {
                return "Недостаточно средств";
            }
        },
        
        getHistory: function() {
            return history;  // возвращаем всю историю
        },
        
