// Оператор Break ВЫХОД ИЗ ЦИКЛА ДОСРОЧНО

// 1. СОЗДАЕМ СЧЕТЧИК ПОПЫТОК
// Переменная tries будет отслеживать, сколько раз пользователь пытался ввести возраст
// Начинаем с 0, потому что пока попыток не было
let tries = 0;

// 2. НАЧИНАЕМ ЦИКЛ WHILE (ПОКА)
// Цикл будет выполняться, ПОКА количество попыток (tries) МЕНЬШЕ 3
// Это значит: максимум 3 попытки (0, 1, 2)
while (tries < 3) {

    // 3. ЗАПРАШИВАЕМ ВОЗРАСТ У ПОЛЬЗОВАТЕЛЯ
    // prompt() - показывает диалоговое окно с вопросом
    // + перед prompt() - преобразует введенный текст в число
    // Пример: если ввести "25" → станет числом 25
    // Если ввести "двадцать пять" или оставить пустым → станет NaN (Not a Number)
    const age = +prompt(`Сколько вам лет?`);

    // 4. ПРОВЕРЯЕМ, КОРРЕКТНО ЛИ ВВЕДЕН ВОЗРАСТ
    // if (age) проверяет: если age НЕ 0, НЕ пустая строка, НЕ NaN, НЕ undefined
    // В JavaScript: 0, "", NaN, undefined, null, false → считаются "ложными" (false)
    // Любое другое число (1, 25, 100) → "истинно" (true)
    if (age) {
        // 5. ЕСЛИ ВОЗРАСТ ВВЕДЕН КОРРЕКТНО
        // alert() показывает всплывающее окно с сообщением
        alert(`Welcome`);

        // 6. ОПЕРАТОР BREAK - ВЫХОД ИЗ ЦИКЛА
        // break НЕМЕДЛЕННО прерывает выполнение цикла while
        // Дальнейший код в цикле (tries++ и alert) НЕ выполняется
        // Управление переходит к строке после цикла (alert('End'))
        break;
    }

    // 7. ЕСЛИ ВОЗРАСТ НЕ ВВЕДЕН КОРРЕКТНО (эта часть выполняется ТОЛЬКО если if (age) был false)
    // Увеличиваем счетчик попыток на 1
    // tries++ эквивалентно tries = tries + 1
    tries++;

    // 8. СООБЩАЕМ ОБ ОШИБКЕ
    // Показываем сообщение об некорректном вводе
    // Эта строка выполняется только при НЕУДАЧНОЙ попытке
    alert(`Данные введены некорректно.`);

    // 9. КОНЕЦ ИТЕРАЦИИ ЦИКЛА
    // Управление возвращается к проверке условия while (tries < 3)
    // Если tries стало равно 3, цикл завершится
    // Если tries меньше 3, начнется новая итерация (новая попытка)
}

