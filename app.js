// 1. ОБЪЯВЛЕНИЕ ФУНКЦИИ getNumberData
// function - ключевое слово для создания функции
// getNumberData - имя функции (мы сами его придумываем)
// (question) - параметр функции (переменная, которая получает значение при вызове) (мы тоже сами его придумываем)
// { ... } - тело функции (код, который выполняется при вызове)
function getNumberData(question) {

    // 2. БЕСКОНЕЧНЫЙ ЦИКЛ WHILE
    // while(true) - цикл, который будет выполняться ВЕЧНО
    // ...пока мы не прервем его вручную (с помощью return или break)
    // Это нужно, чтобы повторять запрос, пока пользователь не введет корректные данные
    while (true) {

        // 3. ЗАПРОС ДАННЫХ У ПОЛЬЗОВАТЕЛЯ
        // prompt(question) - показывает диалоговое окно с вопросом
        // + перед prompt() - преобразует введенную строку в число
        // Примеры преобразования:
        //   "+prompt('Введите число')" → если ввести "25" → станет числом 25
        //   "+prompt('Введите число')" → если ввести "abc" → станет NaN (Not a Number)
        //   "+prompt('Введите число')" → если ввести "" (пусто) → станет 0
        // res - переменная, в которую сохраняется результат
        const res = +prompt(question);

        // 4. ПРОВЕРКА КОРРЕКТНОСТИ ВВОДА
        // if (res === 0 || res) - сложное условие, разберем его по частям:
        // 
        // ЧАСТЬ 1: res === 0
        //   Проверяет: введено ли число 0?
        //   Если пользователь ввел "0" или нажал OK на пустом поле → res = 0
        //   Тогда res === 0 будет true
        //
        // ЧАСТЬ 2: || - логическое ИЛИ
        //   Условие истинно, если ХОТЯ БЫ ОДНА из частей истинна
        //
        // ЧАСТЬ 3: res (без сравнения)
        //   В JavaScript любое значение может быть "истинным" (truthy) или "ложным" (falsy)
        //   Ложные значения: 0, NaN, "", undefined, null, false
        //   Истинные значения: все остальные числа, непустые строки и т.д.
        //   Так что если res = 25 (любое число кроме 0), то условие "res" будет true
        //   Но если res = NaN (пользователь ввел не число), то "res" будет false
        //
        // ИТОГ УСЛОВИЯ: если пользователь ввел:
        //   - 0 → true (первая часть)
        //   - Любое другое число (5, 10, 100) → true (вторая часть)  
        //   - Не число (NaN) или отменил ввод → false
        if (res === 0 || res) {
            // 5. ОПЕРАТОР RETURN - ВОЗВРАТ ЗНАЧЕНИЯ И ВЫХОД ИЗ ФУНКЦИИ
            // return выполняет две задачи:
            //   1. Возвращает значение res тому, кто вызвал функцию
            //   2. НЕМЕДЛЕННО завершает выполнение функции
            //   3. Также прерывает цикл while(true)
            // После return выполнение функции останавливается
            return res;
        }

        // 6. ЧТО БУДЕТ, ЕСЛИ УСЛОВИЕ if БЫЛО FALSE?
        // Если пользователь ввел некорректные данные (NaN или отменил):
        // - Условие if (res === 0 || res) будет false
        // - return НЕ выполнится
        // - Цикл while(true) начнет новую итерацию
        // - Снова покажется prompt с тем же вопросом
        // И так будет повторяться, пока пользователь не введет корректные данные
    }

    // 7. ЭТА СТРОКА НИКОГДА НЕ ВЫПОЛНИТСЯ!
    // Потому что цикл while(true) либо вечный, либо прерывается return
}

