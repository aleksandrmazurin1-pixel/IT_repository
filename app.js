//Loop (basics)  ---  Пока мы не дошли до конца массива — выводи в консоль следующий элемент и переходи к следующему

// 1. Создаем массив (список) с именем "numbers" и помещаем в него шесть чисел.
const numbers = [1, 2, 5, 7, 8, 11];

// 2. Создаем переменную-счетчик "index" (указатель) и устанавливаем ее в 0.
// Это наша "текущая позиция" в списке. В программировании счет почти всегда начинается с 0.
let index = 0;

// 3. Начинаем цикл WHILE (ПОКА).
// Условие: ПОКА значение переменной index МЕНЬШЕ, чем длина массива numbers (numbers.length = 6).
// То есть, цикл будет работать, пока index = 0, 1, 2, 3, 4, 5. Как только index станет 6, цикл остановится.
while (index < numbers.length) {
    // 4. Действие внутри цикла:
    // Обращаемся к элементу массива "numbers" на позиции "index" и выводим его значение в консоль.
    // При первой итерации: index = 0 -> numbers[0] -> выведет 1.
    console.log(numbers[index]);

    // 5. Критически важный шаг: Увеличиваем счетчик "index" на 1.
    // Это значит: "теперь переходи к следующему элементу в списке".
    // Без этой строки значение index всегда было бы 0, условие (0 < 6) всегда было бы true,
    // и цикл бы выполнялся БЕСКОНЕЧНО, выводя только первую цифру "1".
    index = index + 1; // Можно сокращенно писать как index++
}

// 6. Эта строка выполнится только ПОСЛЕ того, как цикл завершил свою работу.
// То есть, когда условие (index < numbers.length) стало false (когда index стал равен 6).
console.log('Hello after while');


