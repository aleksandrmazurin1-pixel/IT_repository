function mergeThree(arr1, arr2, arr3) {
    let mergeTwo = merge(arr1, arr2);
    if (mergeTwo.length === 0) return arr3;
    if (arr3.length === 0) return mergeTwo;

    if (mergeTwo[0] < arr3[0]) {
        return [mergeTwo[0], ...mergeThree(mergeTwo.slice(1), arr3, [])]; //[] в рекурсии — это заглушка для третьего массива, когда он уже пуст. Мы говорим функции: "Продолжай работать с первыми двумя массивами, а третий мы уже обработали, он пустой"
    } else {
        return [arr3[0], ...mergeThree(mergeTwo, arr3.slice(1), [])];
    }
}
