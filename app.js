// ЗАДАНИЕ 2: Напиши рекурсивную функцию multiplyArray
// Она должна умножать все элементы массива

function multiplyArray(arr) {
    // 1. Базовый случай: пустой массив
    if (arr.length === 0) {
        return 1;  // Почему 1? Потому что 1 * x = x (нейтральный элемент)
    }
    
    // 2. Берем первый элемент
    const first = arr[0];
    
    // 3. Берем остальные элементы (массив без первого)
    const rest = arr.slice(1);
    
    // 4. Рекурсивный шаг: первый * произведение остальных
    return first * multiplyArray(rest);
}

console.log(multiplyArray([2, 3, 4])); // 24

// 🔍 Как работает:
// multiplyArray([2, 3, 4])
// = 2 * multiplyArray([3, 4])
// = 2 * (3 * multiplyArray([4]))
// = 2 * (3 * (4 * multiplyArray([])))
// = 2 * (3 * (4 * 1))   ← Базовый случай: multiplyArray([]) = 1
// = 2 * (3 * 4)
// = 2 * 12
// = 24




