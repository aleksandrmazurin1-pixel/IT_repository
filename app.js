function createHabitTracker(habitName, goal) {
    let todayProgress = 0;  // понятное имя
    
    return {
        getName: function() {
            return habitName;
        },
        
        getGoal: function() {
            return goal;
        },
        
        logProgress: function(amount) {
            let newTotal = todayProgress + amount;
            
            if (newTotal > goal) {
                todayProgress = newTotal;
                return "Цель на сегодня достигнута! Остальное - перевыполнение";
            } else {
                todayProgress = newTotal;
                return todayProgress;
            }
        },
        
        getTodayProgress: function() {
            return todayProgress;  // ✅ правильная переменная
        },
        
        resetDay: function() {
            todayProgress = 0;  // ✅ сбрасываем
            // return не нужен
        },
        
        getStats: function() {
            let remaining = goal - todayProgress;
            if (remaining < 0) remaining = 0;
            
            return {
                name: habitName,
                goal: goal,
                today: todayProgress,
                remaining: remaining
            };
        }
    };
}



