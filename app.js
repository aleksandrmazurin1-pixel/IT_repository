function reverseNumber(n) {
    // Базовый случай: если одна цифра
    if (n < 10) return n;
    
    // Получаем последнюю цифру
    let lastDigit = n % 10;
    
    // Получаем остаток
    let rest = Math.floor(n / 10);
    
    // Узнаем длину остатка (сколько цифр в rest)
    let length = String(rest).length;
    
    // Формула: последняя цифра * 10^длина + перевернутый остаток
    return lastDigit * Math.pow(10, length) + reverseNumber(rest);
}

