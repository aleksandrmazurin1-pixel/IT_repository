// Цикл for

// 1. СОЗДАЕМ МАССИВ ЧИСЕЛ
// Объявляем константу numbers и присваиваем ей массив из 6 чисел
// Массив - это упорядоченный список значений
const numbers = [1, 2, 54, 88, 92, 100];

// 2. НАЧИНАЕМ ЦИКЛ FOR
// Цикл for состоит из трех частей в круглых скобках:
// for(начальное_значение; условие_продолжения; шаг_изменения)
for (let i = 0; i < numbers.length; i++) {
    // ↓ Часть 1: ИНИЦИАЛИЗАЦИЯ (выполняется один раз в начале)
    // let i = 0 - создаем переменную-счетчик i и устанавливаем ее в 0
    // i будет отслеживать текущую позицию в массиве

    // ↓ Часть 2: УСЛОВИЕ ПРОДОЛЖЕНИЯ (проверяется перед каждой итерацией)
    // i < numbers.length - цикл будет выполняться, пока i меньше длины массива
    // numbers.length = 6 (в массиве 6 элементов), значит i будет: 0, 1, 2, 3, 4, 5

    // ↓ Часть 3: ИЗМЕНЕНИЕ СЧЕТЧИКА (выполняется после каждой итерации)
    // i++ - увеличиваем i на 1 после выполнения тела цикла
    // Это эквивалентно i = i + 1

    // 3. ТЕЛО ЦИКЛА (выполняется на каждой итерации)
    // numbers[i] *= 2; - это сокращенная запись numbers[i] = numbers[i] * 2;
    // Мы берем элемент массива на позиции i и умножаем его на 2
    // Результат сохраняем обратно в тот же элемент массива
    numbers[i] *= 2;

    // 4. ВЫВОД РЕЗУЛЬТАТА
    // Выводим в консоль уже измененное значение элемента
    // На первой итерации: numbers[0] = 1 * 2 = 2 → выводим 2
    // На второй итерации: numbers[1] = 2 * 2 = 4 → выводим 4
    // И так далее...
    console.log(numbers[i]);

    // 5. НЕЯВНЫЙ ШАГ: ВОЗВРАТ К НАЧАЛУ ЦИКЛА
    // Здесь автоматически выполняется i++ (из части 3)
    // И затем снова проверяется условие i < numbers.length
}

// 6. КОНЕЦ ЦИКЛА
// Эта строка выполняется только после того, как цикл завершил ВСЕ итерации
// То есть когда i стало равно 6, условие i < 6 стало false
console.log('End for loop');

