        if (!userAnswer || userAnswer.trim() === '') {
            // Если поле пустое или только пробелы
            alert("Напишите фильм!");
            return false; // Выходим из метода
        }
        
        // 3.3. УМЕНЬШАЕМ КОЛИЧЕСТВО ПОПЫТОК
        this.tries--; // Было 3 → станет 2, было 2 → станет 1 и т.д.
        
