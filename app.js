// ЗАДАНИЕ: Напиши рекурсивную функцию countDown
// Она должна выводить числа от n до 1

function countDown(n) {
    // 1. Базовый случай: когда дошли до 0 - заканчиваем
    if (n === 0) {
        console.log(0);  // Выводим 0
        return;          // Завершаем рекурсию
    }
    
    // 2. Сначала выводим текущее число
    console.log(n);
    
    // 3. Рекурсивный шаг: вызываем для n-1
    countDown(n - 1);
}

countDown(5);
// Выведет: 5, 4, 3, 2, 1, 0

