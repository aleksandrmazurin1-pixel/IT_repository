// Напиши рекурсивную функцию mergeArrays(arr1, arr2), которая объединяет 
// два массива в один, чередуя элементы: сначала первый элемент из первого 
// массива, затем первый из второго, затем второй из первого и так далее.


function mergeArrays(arr1, arr2) {
    // Базовые случаи
    if (arr1.length === 0) return arr2;
    if (arr2.length === 0) return arr1;

    // Берем первые элементы из обоих массивов
    let first1 = arr1[0];
    let first2 = arr2[0];
    
    // Остатки массивов (оба уменьшаем!)
    let rest1 = arr1.slice(1);
    let rest2 = arr2.slice(1);
    
    // Рекурсивно объединяем остатки (ОБОИХ массивов!)
    let mergedRest = mergeArrays(rest1, rest2);
    
    // Собираем результат: первый из arr1, первый из arr2, затем остатки
    return [first1, first2, ...mergedRest];
}


