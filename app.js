//     и справа налево).

function isPalindrome(arr) {
    if (arr.length <= 1) return true;

    let first = arr[0];
    let last = arr[arr.length - 1];
    let lastRec = arr.slice(1, arr.length - 1);

    if (first === last) {
        return isPalindrome(lastRec);
    } else {
        return false;
    }
}

