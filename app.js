// В бесконечном цикле запрашивает у пользователя цены товаров
// Когда пользователь вводит "0" или оставляет поле пустым — цикл завершается
// Функция возвращает объект с названием магазина и общей суммой покупок

function calculateTotal(storeName) {
    let total = 0; // Переменная для накопления суммы

    while (true) {
        // Запрашиваем цену товара
        const input = prompt(`Введите цену товара для ${storeName} (0 для завершения):`);

        // Проверяем, хочет ли пользователь закончить
        if (input === "0" || input === "" || input === null) {
            break; // Выходим из цикла
        }

        // Преобразуем введенный текст в число
        const price = +input;

        // Проверяем, что введено корректное число
        if (!isNaN(price) && price > 0) {
            total += price; // Добавляем к общей сумме
        } else {
            alert("Пожалуйста, введите корректную цену!");
        }
    }

    // Возвращаем объект с результатами
    return {
        store: storeName,
        total: total
    };
}

