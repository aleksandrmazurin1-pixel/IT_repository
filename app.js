function quickSortWithPivot(arr, pivotStrategy) {

    if (arr.length <= 1) return arr;

    let pivotIndex;
    let pivot;

    if (pivotStrategy === 'first') {
        pivotIndex = 0;
    } else if (pivotStrategy === 'last') {
        pivotIndex = arr.length - 1;
    } else if (pivotStrategy === 'middle') {
        pivotIndex = Math.floor(arr.length / 2);
    }

    let rest = [];

    for (let i = 1; i < arr.length; i++) { 
        if (pivotIndex !== i) {
            rest.push(arr[i]);
        }
    }
    pivot = arr[pivotIndex];
    let left = [];
    let right = [];

    for (let j = 0; j < rest.length; j++) {
        if (rest[j] < pivot) {
            left.push(rest[j]);
        } else {
            right.push(rest[j]);
        }
    }

    let leftSort = quickSortWithPivot(left, pivotStrategy);
    let rightSort = quickSortWithPivot(right, pivotStrategy);

    return [...leftSort, pivot, ...rightSort];

}


